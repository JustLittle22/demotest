list_day_so = [20,55,44,85,96,75,21,3,5,7,65]

# Sắp xếp theo thứ tự tăng dần
list_day_so.sort()
print("Hai số lớn nhất là: ", list_day_so[-1], 'và', list_day_so[-2])

# Sắp xếp theo thứ tự giảm dần
list_day_so.sort(reverse = True)
print("Hai số lớn nhất là: ", list_day_so[1], 'và', list_day_so[0])